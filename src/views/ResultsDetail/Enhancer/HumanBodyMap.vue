  'Esophagus': 'st16',
  'Nerve': 'st56',
  'Muscle tissue': 'st12',
  'Bone': 'st48',
  'Soft tissue': 'st65',
  'Bronchus': 'st27'
})

// 计算当前需要高亮的类名列表
const highlightedClasses = computed(() => {
  return props.highlightedOrgans.map(organ => organToClassMap.value[organ])
})

// 检查元素是否应该高亮
const isHighlighted = (elementClass) => {
  return highlightedClasses.value.includes(elementClass)
}

// 获取器官对应的类名
const getClassForOrgan = (organName) => {
  return organToClassMap.value[organName] || ''
}

// 创建反向映射，从class名映射到器官名
const classToOrganMap = computed(() => {
  const map = {}
  Object.entries(organToClassMap.value).forEach(([organ, className]) => {
    map[className] = organ
  })
  return map
})

// 处理点击事件
const handleClick = (event) => {
  // 获取被点击的元素
  const clickedElement = event.target
  
  // 获取元素的class名
  const elementClasses = clickedElement.classList
  
  // 检查是否点击了某个器官
  for (const className of elementClasses) {
    if (className.startsWith('st') && classToOrganMap.value[className]) {
      // 找到对应的器官名称
      const organName = classToOrganMap.value[className]
      // 发出事件，传递器官名称
      emit('organClick', organName)
      break
    }
  }
}

